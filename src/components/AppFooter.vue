import { ref } from "vue";
import { useI18n } from "vue-i18n";

const { t } = useI18n();
const emailText = ref(null);

const copyToClipboard = (email) => {
  navigator.clipboard
    .writeText(email)
    .then(() => {
      if (emailText.value) {
        emailText.value.textContent = t("footer.copy");
        setTimeout(() => {
          if (emailText.value) {
            emailText.value.textContent = email;
          }
        }, 2000);
      }
    })
    .catch((err) => {
      console.error("Could not copy text: ", err);
    });
};
</script>

